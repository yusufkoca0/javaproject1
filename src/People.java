
public class People {
    public String userid;
    public String name;
    public String surname;
    public String country;
    public String PersonType;


    public People() {
        this.userid = "";
        this.name = "";
        this.surname = "";
        this.country = "";
    }

    public People(String userid, String name, String surname, String country){
        this.userid = userid;
        this.name = name;
        this.surname = surname;
        this.country = country;
    }

}

